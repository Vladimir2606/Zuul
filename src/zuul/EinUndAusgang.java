package zuul;

public class EinUndAusgang {
	private Raum raum;
	private String bezeichnung;
	
	public EinUndAusgang(Raum raum, String bezeichnung) {
		this.raum = raum;
		this.bezeichnung = bezeichnung;
	}

	public Raum getRaum() {
		return raum;
	}

	public String getBezeichnung() {
		return bezeichnung;
	}
	

	
	
	

}
